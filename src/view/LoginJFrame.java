    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        // TODO add your handling code here:
        if (checkLogin()) {
            String userName = this.txtUser.getText().trim();
            String pass = new String(this.txtPass.getPassword());

                int status = this.loginSvc.login(userName, pass);
                if (status == 1) {
                    ProductJFrame qlDiem = new ProductJFrame();
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công",
                            "Login success", JOptionPane.INFORMATION_MESSAGE);
                    qlDiem.setVisible(true);
                    this.dispose();
                } else if (status == 0) {
                    NhapXuatJFrame qlSv = new NhapXuatJFrame();
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công",
                            "Login success", JOptionPane.INFORMATION_MESSAGE);
                    qlSv.setVisible(true);
                    this.dispose();
                } else {
                    if (dem == 5) {
                        JOptionPane.showMessageDialog(this, "Đăng nhập thất bại\n"
                            + "Sai mật khẩu hoặc tên đăng nhập", "Login fail", JOptionPane.ERROR_MESSAGE);
                        System.exit(0);
                    } else {
                        JOptionPane.showMessageDialog(this, "Đăng nhập thất bại\n"
                            + "Sai mật khẩu hoặc tên đăng nhập \n" + "Bạn còn lại " + (5 - dem) + "/5 số lần đăng nhập", "Login fail", JOptionPane.ERROR_MESSAGE);
                        dem++;
                    }
                    
                }

        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

